#include "fdf.h"

int		verif_line(char *line)
{
	int		i;

	i = 0;
	while (line[i] != '\0')
	{
		if ((!ft_isdigit(line[i]) && line[i] != ' ') && line[i] != '-')
			return (0);
		i++;
	}
	return (1);
}
